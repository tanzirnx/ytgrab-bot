    @classmethod
    def get(cls, key: str, lang: str = "en", **kwargs) -> str:
        """Get translated string."""
        lang = lang if lang in cls.STRINGS else "en"
        template = cls.STRINGS.get(lang, cls.STRINGS["en"]).get(
            key, cls.STRINGS["en"].get(key, key)
        )
        try:
            return template.format(**kwargs) if kwargs else template
        except (KeyError, IndexError):
            return template

    @classmethod
    def get_available_languages(cls) -> dict:
        """Get all available languages."""
        from utils.constants import SUPPORTED_LANGUAGES
        return {k: v for k, v in SUPPORTED_LANGUAGES.items() if k in cls.STRINGS}


